#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	unsigned int	i;
	unsigned int	len;

	if (!dst || !src)
		return (0);
	i = 0;
	len = ft_strlen(src);
	if (size <= 0)
		return (len);
	while (src[i] && i + 1 < size)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = 0;
	return (len);
}
